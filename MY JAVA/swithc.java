public class swithc {
    public static void main(String[] args) {
        int i = 1;
        switch (i) {
            case 0:
                int j = 0;
                System.out.print(j);
            case 1:
                j = 1;
                System.out.print(j);
            case 2:
                j = 2;
                System.out.print(j);
            default:
                j = -1;
                System.out.print(j);
        }
    }
    
}
